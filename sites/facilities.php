    <p><h1> KKSL Library</h1><br>
  &nbsp&nbsp&nbsp&nbsp&nbsp&nbspThe Kolej Komuniti Selandar Library was officially opened in 2006. It is located on the first floor of the administrative block and has a
total collection of 13,000 books from various categories. Only KKSL employees and students can borrow books as members. Members intending to borrow library materials must present their
student or employee card at the counter. The borrowing duration for one book is seven days for members. </p>

<p><b> Operating hours</b><br>
Monday - Friday	: 8.00am - 9.00pm <br>
Saturday	: 9.00am - 5.00pm <br>
Sunday & Public Holidays	: Closed <br>
